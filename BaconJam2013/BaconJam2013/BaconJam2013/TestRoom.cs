        public TestRoom()
        {
            _player = new Player(new Vector2(320, 0));
            Viewport.Follow(_player);

            _platforms = new List<Platform>();

            for (int row = 0; row < 30; ++row)
            {
                for (int col = 0; col < 40; ++col)
                {
                    if (_map[row, col] == 1)
                    {
                        _platforms.Add(new Platform(new Vector2(col * 48, row * 48), new Vector2(48), false));
                    }
                }
            }

            Core.UpdateEvent += Update;
            Core.RenderEvent += Render;
        }

        public void Update(object sender, UpdateData data)
        {
            Vector2 startPos = _player.Pos;
            bool below = false;

            for (int i = 0; i < _platforms.Count; ++i)
            {
                Platform plat = _platforms[i];

                Rectangle
                    playerBounds = _player.Bounds(),
                    platBounds = plat.Bounds();

                if (!below)
                {
                    if (platBounds.Intersects(new Rectangle(playerBounds.X, playerBounds.Bottom, playerBounds.Width, 2)))
                        below = true;
                }

                Side hitSide = Util.Collide(playerBounds, platBounds, _player.Vel);

                if (plat.JumpThrough)
                {
                    if (hitSide != Side.None && playerBounds.Bottom <= platBounds.Top)
                    {
                        hitSide = Side.Bottom;
                    }
                    else
                    {
                        hitSide = Side.None;
                    }
                }

                switch (hitSide)
                {
                    case Side.Top:

                        _player.Pos.Y = platBounds.Bottom + _player.Origin.Y;
                        _player.Vel.Y = 0.0f;

                        break;
                    case Side.Bottom:

                        _player.Pos.Y = platBounds.Top - playerBounds.Height + _player.Origin.Y;
                        _player.Vel.Y = 0.0f;
                        _player.VertState = VertState.Ground;

                        break;
                    case Side.Left:

                        _player.Pos.X = platBounds.Left - playerBounds.Width + _player.Origin.X - 1;
                        _player.Vel.X = 0.0f;

                        break;
                    case Side.Right:

                        _player.Pos.X = platBounds.Right + _player.Origin.X + 1;
                        _player.Vel.X = 0.0f;

                        break;
                }
            }

            if (_player.VertState == VertState.Ground && !below)
            {
                _player.VertState = VertState.Air;
            }
        }

